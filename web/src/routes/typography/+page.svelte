  <h1>Heading 1</h1>
  <p>
    Every friend I have with a job that involves picking up something heavier than a laptop more
    than twice a week eventually finds a way to slip something like this into conversation: “Bro,
    you don’t work hard. I just worked a 4700-hour week digging a tunnel under Mordor with a
    screwdriver.” They have a point. Mordor sucks, and it’s certainly more physically taxing to dig
    a tunnel than poke at a keyboard unless you’re an ant. But, for the sake of the argument, can we
    agree that stress and insanity are bad things? Awesome. Welcome to programming.
  </p>
  <h2>Heading 2</h2>
  <p>
    Imagine joining an engineering team. You’re excited and full of ideas, probably just out of
    school and a world of clean, beautiful designs, awe-inspiring in their aesthetic unity of
    purpose, economy, and strength. You start by meeting Mary, project leader for a bridge in a
    major metropolitan area. Mary introduces you to Fred, after you get through the fifteen security
    checks installed by Dave because Dave had his sweater stolen off his desk once and Never Again.
    Fred only works with wood, so you ask why he’s involved because this bridge is supposed to allow
    rush-hour traffic full of cars full of mortal humans to cross a 200-foot drop over rapids. Don’t
    worry, says Mary, Fred’s going to handle the walkways. What walkways? Well Fred made a good case
    for walkways and they’re going to add to the bridge’s appeal. Of course, they’ll have to be
    built without railings, because there’s a strict no railings rule enforced by Phil, who’s not an
    engineer. Nobody’s sure what Phil does, but it’s definitely full of synergy and has to do with
    upper management, whom none of the engineers want to deal with so they just let Phil do what he
    wants. Sara, meanwhile, has found several hemorrhaging-edge paving techniques, and worked them
    all into the bridge design, so you’ll have to build around each one as the bridge progresses,
    since each one means different underlying support and safety concerns. Tom and Harry have been
    working together for years, but have an ongoing feud over whether to use metric or imperial
    measurements, and it’s become a case of “whoever got to that part of the design first.” This has
    been such a headache for the people actually screwing things together, they’ve given up and just
    forced, hammered, or welded their way through the day with whatever parts were handy. Also, the
    bridge was designed as a suspension bridge, but nobody actually knew how to build a suspension
    bridge, so they got halfway through it and then just added extra support columns to keep the
    thing standing, but they left the suspension cables because they’re still sort of holding up
    parts of the bridge. Nobody knows which parts, but everybody’s pretty sure they’re important
    parts. After the introductions are made, you are invited to come up with some new ideas, but you
    don’t have any because you’re a propulsion engineer and don’t know anything about bridges.
  </p>
  <p>
    Would you drive across this bridge? No. If it somehow got built, everybody involved would be
    executed. Yet some version of this dynamic wrote every single program you have ever used,
    banking software, websites, and a ubiquitously used program that was supposed to protect
    information on the internet but didn’t.
  </p>
  <h3>Heading 3</h3>
  <p>
    Every programmer occasionally, when nobody’s home, turns off the lights, pours a glass of
    scotch, puts on some light German electronica, and opens up a file on their computer. It’s a
    different file for every programmer. Sometimes they wrote it, sometimes they found it and knew
    they had to save it. They read over the lines, and weep at their beauty, then the tears turn
    bitter as they remember the rest of the files and the inevitable collapse of all that is good
    and true in the world.
  </p>
  <p>
    This file is Good Code. It has sensible and consistent names for functions and variables. It’s
    concise. It doesn’t do anything obviously stupid. It has never had to live in the wild, or
    answer to a sales team. It does exactly one, mundane, specific thing, and it does it well. It
    was written by a single person, and never touched by another. It reads like poetry written by
    someone over thirty.
  </p>
  <p>
    Every programmer starts out writing some perfect little snowflake like this. Then they’re told
    on Friday they need to have six hundred snowflakes written by Tuesday, so they cheat a bit here
    and there and maybe copy a few snowflakes and try to stick them together or they have to ask a
    coworker to work on one who melts it and then all the programmers’ snowflakes get dumped
    together in some inscrutable shape and somebody leans a Picasso on it because nobody wants to
    see the cat urine soaking into all your broken snowflakes melting in the light of day. Next
    week, everybody shovels more snow on it to keep the Picasso from falling over.
  </p>
  <h4>Heading 4</h4>
  <p>
    There’s a theory that you can cure this by following standards, except there are more
    “standards” than there are things computers can actually do, and these standards are all
    variously improved and maligned by the personal preferences of the people coding them, so no
    collection of code has ever made it into the real world without doing a few dozen identical
    things a few dozen not even remotely similar ways. The first few weeks of any job are just
    figuring out how a program works even if you’re familiar with every single language, framework,
    and standard that’s involved, because standards are unicorns.
  </p>
  <p>
    I spent a few years growing up with a closet in my bedroom. The closet had an odd design. It
    looked normal at first, then you walked in to do closet things, and discovered that the wall on
    your right gave way to an alcove, making for a handy little shelf. Then you looked up, and the
    wall at the back of the alcove gave way again, into a crawlspace of utter nothingness, where no
    light could fall and which you immediately identified as the daytime retreat for every ravenous
    monster you kept at bay with flashlights and stuffed animals each night.
  </p>
  <h5>Heading 5</h5>
  <p>
    This is what it is to learn programming. You get to know your useful tools, then you look
    around, and there are some handy new tools nearby and those tools show you the bottomless horror
    that was always right next to your bed.
  </p>
  <p>
    For example, say you’re an average web developer. You’re familiar with a dozen programming
    languages, tons of helpful libraries, standards, protocols, what have you. You still have to
    learn more at the rate of about one a week, and remember to check the hundreds of things you
    know to see if they’ve been updated or broken and make sure they all still work together and
    that nobody fixed the bug in one of them that you exploited to do something you thought was
    really clever one weekend when you were drunk. You’re all up to date, so that’s cool, then
    everything breaks.
  </p>
